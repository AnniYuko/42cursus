#include "libft.h"

size_t	ft_strlcat(char *dest, const char *src, size_t destsize)
{
	size_t	i;
	size_t	destlen;
	size_t	srclen;

	i = 0;
	destlen = ft_strlen(dest);
	srclen = ft_strlen(src);
	while (i < (destsize - 1))
	{
		dest[i++] = *src++;
	}
	if (destsize != 0)
		dest[i] = '\0';
	return (destlen + srclen);
}
